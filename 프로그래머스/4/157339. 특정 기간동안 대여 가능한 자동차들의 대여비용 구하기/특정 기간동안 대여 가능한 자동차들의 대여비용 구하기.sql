SELECT DISTINCT
    CAR_ID,
    CAR_TYPE,
    TOTAL_30_FEE AS FEE
FROM
(SELECT
    C.CAR_ID,
    C.CAR_TYPE,
    c.daily_fee,
    CASE 
        WHEN C.CAR_TYPE = '세단' THEN 10
        WHEN C.CAR_TYPE = 'SUV' THEN 8
    END AS DISCOUNT_RATE,
    truncate(C.DAILY_FEE * 30 * (100-d.DISCOUNT_RATE) * 0.01, 0) AS TOTAL_30_FEE
    # (C.DAILY_FEE * 30 * (100 - 
    #     CASE 
    #         WHEN C.CAR_TYPE = '세단' THEN 10
    #         WHEN C.CAR_TYPE = 'SUV' THEN 8 
    #     END
    #     )* 0.01) AS TOTAL_30_FEE
FROM CAR_RENTAL_COMPANY_CAR C join car_rental_company_discount_plan d
    on c.car_type = d.car_type
WHERE d.duration_type = 30 and C.CAR_TYPE IN ('세단','SUV') AND C.CAR_ID NOT in (SELECT
    CAR_ID
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE (END_DATE >= '2022-11-01' AND START_DATE <= '2022-11-30'))) AS TABLE_1

WHERE TABLE_1.TOTAL_30_FEE >= 500000
AND TABLE_1.TOTAL_30_FEE <2000000
ORDER BY TOTAL_30_FEE DESC, CAR_TYPE ASC, CAR_ID DESC;